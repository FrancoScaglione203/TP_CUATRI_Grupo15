create procedure ModificarPerfil
  @id int,
  @clave varchar(1000),
  @TipoUsuario int,
  @Nombre varchar(1000),
  @Apellido varchar(1000),
  @Email varchar(1000),
  @Provincia varchar(1000),
  @Localidad varchar(1000)
AS
BEGIN
  UPDATE USUARIOS
    SET Nombre = @Nombre,
        Apellido = @Apellido,
		Clave = @clave,
		TipoUsuario = @TipoUsuario,
		Email = @Email,
		Provincia = @Provincia,
		Localidad = @Localidad
    WHERE Id = @Id;
END

Go
create procedure BajaLogica
  @id int
AS
BEGIN
  UPDATE USUARIOS
    SET Estado = 0
    WHERE Id = @Id;
END

